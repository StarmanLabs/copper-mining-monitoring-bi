def build_powerbi_visual_binding_catalog() -> pd.DataFrame:
    catalog = pd.DataFrame(POWERBI_VISUAL_BINDING_CATALOG)

    def _map_dimensions(value: object) -> str:
        if pd.isna(value):
            return ""
        dimension_tokens = [token.strip() for token in str(value).split(";")]
        return "; ".join(VISUAL_DIMENSION_REFERENCE_MAP.get(token, token) for token in dimension_tokens if token)

    catalog["key_dimensions"] = catalog["key_dimensions"].map(_map_dimensions)
    return catalog


