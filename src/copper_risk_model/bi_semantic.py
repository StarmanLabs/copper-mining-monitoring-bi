def build_powerbi_relationship_catalog() -> pd.DataFrame:
    relationship_catalog = pd.DataFrame(POWERBI_RELATIONSHIP_CATALOG)
    relationship_catalog["relationship_name"] = relationship_catalog.apply(
        lambda row: f"{row['from_table']}[{row['from_column']}] -> {row['to_table']}[{row['to_column']}]",
        axis=1,
    )
    relationship_catalog[["tmdl_from_table", "tmdl_from_column", "tmdl_to_table", "tmdl_to_column"]] = pd.DataFrame(
        relationship_catalog.apply(
            lambda row: _relationship_tmdl_refs(
                from_table=str(row["from_table"]),
                from_column=str(row["from_column"]),
                to_table=str(row["to_table"]),
                to_column=str(row["to_column"]),
                cardinality=str(row["cardinality"]),
            ),
            axis=1,
        ).tolist(),
        index=relationship_catalog.index,
    )
    relationship_catalog["from_cardinality"] = relationship_catalog["cardinality"].map(
        {
            "one_to_many": "many",
        }
    )
    relationship_catalog["to_cardinality"] = relationship_catalog["cardinality"].map(
        {
            "one_to_many": "one",
        }
    )
    relationship_catalog["tmdl_cross_filtering_behavior"] = relationship_catalog["cross_filter_direction"].map(
        {
            "single": "oneDirection",
            "both": "bothDirections",
        }
    )
    relationship_catalog["tmdl_from_column_reference"] = relationship_catalog.apply(
        lambda row: f"{row['tmdl_from_table']}.{row['tmdl_from_column']}",
        axis=1,
    )
    relationship_catalog["tmdl_to_column_reference"] = relationship_catalog.apply(
        lambda row: f"{row['tmdl_to_table']}.{row['tmdl_to_column']}",
        axis=1,
    )
    ordered_columns = [
        "relationship_order",
        "relationship_name",
        "from_table",
        "from_column",
        "to_table",
        "to_column",
        "cardinality",
        "from_cardinality",
        "to_cardinality",
        "cross_filter_direction",
        "tmdl_cross_filtering_behavior",
        "active_flag",
        "tmdl_from_table",
        "tmdl_from_column",
        "tmdl_to_table",
        "tmdl_to_column",
        "tmdl_from_column_reference",
        "tmdl_to_column_reference",
        "relationship_scope",
        "modeling_note",
    ]
    return relationship_catalog.loc[:, ordered_columns]


