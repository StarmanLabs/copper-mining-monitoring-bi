def _relationship_tmdl_refs(
    from_table: str,
    from_column: str,
    to_table: str,
    to_column: str,
    cardinality: str,
) -> tuple[str, str, str, str]:
    if cardinality == "one_to_many":
        tmdl_from_table = to_table
        tmdl_from_column = to_column
        tmdl_to_table = from_table
        tmdl_to_column = from_column
    else:
        tmdl_from_table = from_table
        tmdl_from_column = from_column
        tmdl_to_table = to_table
        tmdl_to_column = to_column
    return (
        tmdl_from_table,
        tmdl_from_column,
        tmdl_to_table,
        tmdl_to_column,
    )


